    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $admin = User::findOrFail($id);

        return view('Admin::account_settings', array('admin' => $admin));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $validator = \Validator::make($request->all(), ['username' => 'required|min:5', 'password' => 'min:5', 'password_confirm' => 'min:5|same:password']);

        if ($validator->fails()) {
            return redirect()->back()->withErrors($validator);
        }

        $user = User::find($id);

        $user->password = bcrypt($request->password);

        if ($user->save()) {
            \Session::flash('success_flash_message', 'Your account has been successfully updated!');
        }

        return redirect()->back();
    }

